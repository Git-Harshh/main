#include<iostream>
using namespace std;

int main(){
    int a = 30, b = 40;
    cout<<"operators in c++"<<endl;
    cout<<"there are multiple operators in c++"<<endl;
    cout<<"the value of a + b is "<<a+b<<endl;
    cout<<"the value of a - b is "<<a-b<<endl;
    cout<<"the value of a * b is "<<a*b<<endl;
    cout<<"the value of a / b is "<<a/b<<endl;
    cout<<"the value of a % b is "<<a%b<<endl;
    cout<<"the value of a ++ is "<<a++<<endl;
    cout<<"the value of a --  is "<<a--<<endl;
    return 0;
};